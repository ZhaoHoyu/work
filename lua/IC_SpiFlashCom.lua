		table.insert(gamma_reg_table,((bin_table[m] << 4 )& 0xFF0)+((bin_table[m-1]>> 4) & 0x0F));

		else
		table.insert(gamma_reg_table,((bin_table[m] << 4 )& 0xFF0)+((bin_table[m-1]>> 4) & 0x0F));
		table.insert(gamma_reg_table,(bin_table[m+1] & 0xFF)+((bin_table[m+2]<< 8) & 0xF00));
		end

   end
   
