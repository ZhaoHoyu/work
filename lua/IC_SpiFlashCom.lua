local function WriteCsv(strFileName, tContent)
	local file = ""
	local line = ""
	
	for i = 1, #tContent do
		for j = 1, #tContent[i] do
			if string.find(tContent[i][j], '[,"]') then
				
				tContent[i][j] = '"' .. string.gsub(tContent[i][j], '"', '""') .. '"';
			end
			line = line .. tContent[i][j] .. ","
		end
		line = line .. "\n"
	end
	
	file = assert(io.open(strFileName, "a+b"));--??????
	file:write(line);
	file:close();
	
end

